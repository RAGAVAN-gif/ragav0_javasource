// literals in java program

public class Literals {
    public static void main(String[] args) {
        // literals 
        int num = 0b101;
        System.out.println(num);

        num = 0x7E;
        System.out.println(num);

        num = 10_00_00_000;
        System.out.println(num);

        char c = 'z';
        System.out.println(c);

        c++;
        System.out.println(c);
    }
}
